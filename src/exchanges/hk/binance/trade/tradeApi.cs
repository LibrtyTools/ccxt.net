                        //    _o.amount = _o.price * _o.quantity;
                        //    _result.result.Add(_o);
                        //}
                    }
                }

                _result.SetResult(_json_result);
            }
            else
            {
                _result.SetResult(_market);
            }

            return _result;
        }

